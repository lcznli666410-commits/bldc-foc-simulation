function script = smo_module_script(sim_params, smo, motor)
    useSmo = double(sim_params.use_smo_feedback);
    script = sprintf(['function y = fcn(u)\n' ...
        '%%#codegen\n' ...
        'persistent ih_a ih_b eh_a eh_b th_h we_h\n' ...
        'if isempty(ih_a), ih_a=0; ih_b=0; eh_a=0; eh_b=0; th_h=0; we_h=0; end\n' ...
        'Vdc=%.17g; dt=%.17g; Rs=%.17g; Ls=%.17g; pp=%.17g; K=%.17g; boundary=%.17g; emfCut=%.17g; speedCut=%.17g; minEmf=%.17g; enableT=%.17g; useSmo=%d;\n' ...
        'va=u(1); vb=u(2); vc=u(3); ia=u(4); ib=u(5); ic=u(6); th_true=u(7); wm_true=u(8); t=u(9);\n' ...
        'v_alpha=(2/3)*(va-0.5*vb-0.5*vc); v_beta=(2/3)*(sqrt(3)/2*vb-sqrt(3)/2*vc);\n' ...
        'i_alpha=(2/3)*(ia-0.5*ib-0.5*ic); i_beta=(2/3)*(sqrt(3)/2*ib-sqrt(3)/2*ic);\n' ...
        'za=K*min(1,max(-1,(ih_a-i_alpha)/boundary)); zb=K*min(1,max(-1,(ih_b-i_beta)/boundary));\n' ...
        'ih_a=ih_a+dt/Ls*(v_alpha-Rs*ih_a-za); ih_b=ih_b+dt/Ls*(v_beta-Rs*ih_b-zb);\n' ...
        'a=dt*emfCut/(1+dt*emfCut); eh_a=eh_a+a*(za-eh_a); eh_b=eh_b+a*(zb-eh_b);\n' ...
        'if sqrt(eh_a*eh_a+eh_b*eh_b) >= minEmf\n' ...
        '    th_lpf=atan2(-eh_a,eh_b); th_raw=mod(th_lpf+atan2(we_h,emfCut),2*pi);\n' ...
        '    dth=atan2(sin(th_raw-th_h),cos(th_raw-th_h)); omega_raw=dth/dt;\n' ...
        '    b=dt*speedCut/(1+dt*speedCut); we_h=we_h+b*(omega_raw-we_h); th_h=th_raw;\n' ...
        'end\n' ...
        'if useSmo && t >= enableT, th_ctrl=th_h; wm_ctrl=we_h/pp; else, th_ctrl=th_true; wm_ctrl=wm_true; end\n' ...
        'err=atan2(sin(th_h-th_true),cos(th_h-th_true));\n' ...
        'y=zeros(5,1); y(1)=th_ctrl; y(2)=wm_ctrl; y(3)=th_h; y(4)=we_h/pp; y(5)=err;\n' ...
        'end\n'], motor.Vdc, sim_params.Ts, motor.Rs, motor.Ls, motor.pole_pairs, ...
        smo.Kslide, smo.boundary, smo.emf_lpf_cutoff, smo.speed_lpf_cutoff, ...
        smo.min_emf, smo.enable_time, useSmo);
end
