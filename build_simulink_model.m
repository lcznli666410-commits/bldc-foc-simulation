function script = motor_model_script(sim_params, motor)
    script = sprintf(['function y = fcn(u)\n' ...
        '%%#codegen\n' ...
        'persistent ia ib ic th wm\n' ...
        'if isempty(ia), ia=0; ib=0; ic=0; th=0; wm=0; end\n' ...
        'dt=%.17g; Rs=%.17g; Ld=%.17g; Lq=%.17g; flux=%.17g; pp=%.17g; J=%.17g; B=%.17g;\n' ...
        'va=u(1); vb=u(2); vc=u(3); TL=u(4); we=wm*pp;\n' ...
        'valpha=(2/3)*(va-0.5*vb-0.5*vc); vbeta=(2/3)*(sqrt(3)/2*vb-sqrt(3)/2*vc);\n' ...
        'vd=valpha*cos(th)+vbeta*sin(th); vq=-valpha*sin(th)+vbeta*cos(th);\n' ...
        'ialpha=(2/3)*(ia-0.5*ib-0.5*ic); ibeta=(2/3)*(sqrt(3)/2*ib-sqrt(3)/2*ic);\n' ...
        'id=ialpha*cos(th)+ibeta*sin(th); iq=-ialpha*sin(th)+ibeta*cos(th);\n' ...
        'did=(vd-Rs*id+we*Lq*iq)/Ld; diq=(vq-Rs*iq-we*Ld*id-we*flux)/Lq;\n' ...
        'idn=id+did*dt; iqn=iq+diq*dt; Te=1.5*pp*(flux*iqn+(Ld-Lq)*idn*iqn);\n' ...
        'wm=wm+(Te-TL-B*wm)/J*dt; th=mod(th+wm*pp*dt,2*pi);\n' ...
        'ialpha=idn*cos(th)-iqn*sin(th); ibeta=idn*sin(th)+iqn*cos(th);\n' ...
        'ia=ialpha; ib=-0.5*ialpha+sqrt(3)/2*ibeta; ic=-0.5*ialpha-sqrt(3)/2*ibeta;\n' ...
        'y=zeros(6,1); y(1)=ia; y(2)=ib; y(3)=ic; y(4)=th; y(5)=wm; y(6)=Te;\n' ...
        'end\n'], sim_params.Ts, motor.Rs, motor.Ld, motor.Lq, motor.flux, motor.pole_pairs, motor.J, motor.B);
end

