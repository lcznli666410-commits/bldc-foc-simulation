%% Local builder helpers
function add_codegen_subsystem(modelName, name, pos, inNames, outNames, scriptText)
    path = [modelName '/' name];
    add_block('built-in/SubSystem', path, 'Position', pos);
    safe_clear_subsys(path);

    nIn = numel(inNames);
    nOut = numel(outNames);

    for k = 1:nIn
        y = 35 + (k-1)*35;
        add_block('built-in/Inport', [path '/' inNames{k}], ...
            'Position', [25 y 55 y+14]);
    end

    add_block('simulink/User-Defined Functions/MATLAB Function', [path '/Fcn'], ...
        'Position', [150 45 260 max(95, 45+nIn*25)]);
    rt = sfroot;
    chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', [path '/Fcn']);
    chart.Script = scriptText;

    if nIn == 1
        add_line(path, [inNames{1} '/1'], 'Fcn/1', 'autorouting', 'smart');
    else
        add_block('simulink/Signal Routing/Mux', [path '/Mux'], ...
            'Inputs', num2str(nIn), ...
            'Position', [95 30 100 45+nIn*35]);
        for k = 1:nIn
            add_line(path, [inNames{k} '/1'], ['Mux/' num2str(k)], 'autorouting', 'smart');
        end
        add_line(path, 'Mux/1', 'Fcn/1', 'autorouting', 'smart');
    end

    if nOut == 1
        add_block('built-in/Outport', [path '/' outNames{1}], ...
            'Position', [335 55 365 69]);
        add_line(path, 'Fcn/1', [outNames{1} '/1'], 'autorouting', 'smart');
    else
        add_block('simulink/Signal Routing/Demux', [path '/Demux'], ...
            'Outputs', num2str(nOut), ...
            'Position', [300 35 305 45+nOut*35]);
        add_line(path, 'Fcn/1', 'Demux/1', 'autorouting', 'smart');
        for k = 1:nOut
            y = 35 + (k-1)*35;
            add_block('built-in/Outport', [path '/' outNames{k}], ...
                'Position', [355 y 385 y+14]);
            add_line(path, ['Demux/' num2str(k)], [outNames{k} '/1'], 'autorouting', 'smart');
        end
    end
end

function add_scope_mux(modelName, scopeName, scopePos, signalPaths, ~)
    muxName = [scopeName '_Mux'];
    add_block('simulink/Signal Routing/Mux', [modelName '/' muxName], ...
        'Inputs', num2str(numel(signalPaths)), ...
        'Position', [scopePos(1)-90 scopePos(2)-10 scopePos(1)-85 scopePos(4)+10]);
    add_block('simulink/Sinks/Scope', [modelName '/' scopeName], ...
        'Position', scopePos);
    
    % Apply visual styles to Scope and Mux
    set_param([modelName '/' scopeName], 'BackgroundColor', '[0.95 0.95 0.95]', 'DropShadow', 'on');
    set_param([modelName '/' muxName], 'ShowName', 'off', 'BackgroundColor', 'black');

    for k = 1:numel(signalPaths)
        add_line(modelName, signalPaths{k}, [muxName '/' num2str(k)], 'autorouting', 'smart');
    end
    add_line(modelName, [muxName '/1'], [scopeName '/1'], 'autorouting', 'smart');
end

%% MATLAB Function block scripts
function script = speed_ref_script(sim_params)
    wRef = sim_params.speed_ref * 2*pi/60;
    slope = wRef / 0.05;
    script = sprintf(['function y = fcn(t)\n' ...
        '%%#codegen\n' ...
        'if t < 0.01\n' ...
        '    y = 0;\n' ...
        'elseif t < 0.06\n' ...
        '    y = %.17g * (t - 0.01);\n' ...
        'else\n' ...
        '    y = %.17g;\n' ...
        'end\n' ...
        'end\n'], slope, wRef);
end

function script = speed_controller_script(sim_params, pid_speed, motor)
    script = sprintf(['function y = fcn(u)\n' ...
        '%%#codegen\n' ...
        'persistent integ counter iq_ref\n' ...
        'if isempty(integ), integ=0; counter=0; iq_ref=0; end\n' ...
        'dt=%.17g; Ts=%.17g; Kp=%.17g; Ki=%.17g; outMax=%.17g; outMin=%.17g;\n' ...
        'w_ref=u(1); w_fb=u(2); counter=counter+dt;\n' ...
        'if counter >= Ts\n' ...
        '    counter=0; err=w_ref-w_fb; integ=integ+Ki*err*Ts; unsat=Kp*err+integ;\n' ...
        '    iq_ref=min(outMax,max(outMin,unsat));\n' ...
        '    if iq_ref ~= unsat, integ=integ-Ki*err*Ts; end\n' ...
        'end\n' ...
        'y=zeros(2,1); y(1)=iq_ref; y(2)=0;\n' ...
        'end\n'], sim_params.Ts, 10*(1/20000), pid_speed.Kp, pid_speed.Ki, motor.max_current, -motor.max_current);
end

function script = current_controller_script(sim_params, pwm, pid_id, pid_iq, motor)
    script = sprintf(['function y = fcn(u)\n' ...
        '%%#codegen\n' ...
        'persistent int_id int_iq counter vd vq\n' ...
        'if isempty(int_id), int_id=0; int_iq=0; counter=0; vd=0; vq=0; end\n' ...
        'dt=%.17g; Ts=%.17g; Vdc=%.17g; Ld=%.17g; Lq=%.17g; flux=%.17g; pp=%.17g;\n' ...
        'id_ref=u(1); iq_ref=u(2); id=u(3); iq=u(4); omega_m=u(5); counter=counter+dt;\n' ...
        'if counter >= Ts\n' ...
        '    counter=0;\n' ...
        '    err=id_ref-id; int_id=int_id+%.17g*err*Ts; unsat=%.17g*err+int_id; vd=min(%.17g,max(%.17g,unsat)); if vd~=unsat, int_id=int_id-%.17g*err*Ts; end\n' ...
        '    err=iq_ref-iq; int_iq=int_iq+%.17g*err*Ts; unsat=%.17g*err+int_iq; vq=min(%.17g,max(%.17g,unsat)); if vq~=unsat, int_iq=int_iq-%.17g*err*Ts; end\n' ...
        '    omega_e=omega_m*pp; vd=vd-omega_e*Lq*iq; vq=vq+omega_e*Ld*id+omega_e*flux;\n' ...
        '    Vmax=Vdc/sqrt(3); Vmag=sqrt(vd*vd+vq*vq); if Vmag>Vmax, vd=vd*Vmax/Vmag; vq=vq*Vmax/Vmag; end\n' ...
        'end\n' ...
        'y=zeros(2,1); y(1)=vd; y(2)=vq;\n' ...
        'end\n'], sim_params.Ts, pwm.Ts, motor.Vdc, motor.Ld, motor.Lq, motor.flux, motor.pole_pairs, ...
        pid_id.Ki, pid_id.Kp, pid_id.max, pid_id.min, pid_id.Ki, ...
        pid_iq.Ki, pid_iq.Kp, pid_iq.max, pid_iq.min, pid_iq.Ki);
end

function script = inv_park_script()
